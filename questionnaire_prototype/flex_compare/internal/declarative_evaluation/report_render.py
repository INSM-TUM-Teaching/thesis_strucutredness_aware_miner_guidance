"""Markdown block renderer for the declarative evaluation result.

Produces a self-contained section that can be appended to the per-miner
Markdown report generated by the declarative miner pipeline. All headline
numbers come from MINERful's MinerFulFitnessCheckStarter (tool-emitted);
derived rates are pure arithmetic on tool-emitted operands.
"""

from __future__ import annotations

from typing import Any, Mapping

_DISCLAIMER_EN = (
    "Declarative proxies are not classical precision metrics — "
    "no established standard exists for Declare (see thesis methodology)."
)


def _fmt_num(val: Any, digits: int = 3) -> str:
    if val is None:
        return "—"
    try:
        if isinstance(val, bool):
            return "yes" if val else "no"
        f = float(val)
        return f"{f:.{digits}f}"
    except Exception:
        return str(val)


def render_markdown(result: Mapping[str, Any]) -> str:
    fitness = result.get("fitness") or {}
    proxies = result.get("precision_proxies") or {}
    minerful = result.get("minerful") or {}

    fitness_metrics = fitness.get("fitness_metrics") or {}
    model_aggregates = fitness.get("model_aggregates") or {}

    avg_fitness = fitness_metrics.get("avg_fitness")
    trace_fit_ratio = fitness_metrics.get("trace_fit_ratio")
    n_constraints = fitness_metrics.get("n_constraints_evaluated")
    n_traces = fitness_metrics.get("n_traces_evaluated")

    model_size = proxies.get("model_size") or {}
    by_template = model_size.get("by_template") or {}

    lines = []
    lines.append("## Declarative evaluation (MINERful Fitness Check)")
    lines.append("")
    lines.append("### Fitness — tool-emitted")
    lines.append("")
    lines.append(f"- **Avg fitness (model):** `{_fmt_num(avg_fitness)}`")
    lines.append(f"- **Trace-fit-ratio:** `{_fmt_num(trace_fit_ratio)}`")
    if n_constraints is not None and n_traces is not None:
        lines.append(f"- Constraints evaluated: {n_constraints}; traces evaluated: {n_traces}.")
    lines.append("")

    lines.append("### Derived ratios")
    lines.append(
        "_Pure reporting ratios — numerator **and** denominator are tool-emitted counts from the MINERful FitnessCheck CSV._"
    )
    lines.append("")
    lines.append("| Indicator | Value |")
    lines.append("|---|---|")
    lines.append(
        f"| Mean trace full satisfaction rate (Strict) | `{_fmt_num(model_aggregates.get('mean_trace_full_satisfaction_rate'))}` |"
    )
    lines.append(
        f"| Mean trace non-violation rate (Conformance) | `{_fmt_num(model_aggregates.get('mean_trace_non_violation_rate'))}` |"
    )
    lines.append(
        f"| Mean trace violation rate | `{_fmt_num(model_aggregates.get('mean_trace_violation_rate'))}` |"
    )
    lines.append(
        f"| Share constraints with zero violations | `{_fmt_num(model_aggregates.get('share_constraints_with_zero_violations'))}` |"
    )
    lines.append("")

    lines.append("### Precision proxies")
    lines.append("")
    lines.append("| Indicator | Value |")
    lines.append("|---|---|")
    lines.append(
        f"| Avg. constraint fulfillment rate | `{_fmt_num(proxies.get('avg_constraint_fulfillment_rate'))}` |"
    )
    if proxies.get("vacuity_check_available"):
        lines.append(
            f"| Non-vacuous satisfaction rate (MINERful) | `{_fmt_num(proxies.get('non_vacuous_satisfaction_rate'))}` |"
        )
    else:
        lines.append("| Non-vacuous satisfaction rate | — *(MINERful vacuity checker not requested or not available)* |")
    lines.append(f"| #Constraints | `{model_size.get('n_constraints', 0)}` |")
    lines.append(f"| #Activities | `{model_size.get('n_activities', 0)}` |")
    lines.append("")

    if by_template:
        lines.append("**Constraints by template category:**")
        lines.append("")
        for cat, count in sorted(by_template.items()):
            lines.append(f"- `{cat}`: {count}")
        lines.append("")

    lines.append(f"> _{_DISCLAIMER_EN}_")
    lines.append("")
    return "\n".join(lines)
